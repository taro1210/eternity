package exB2;

public class Main_1_2_3 {
	public static void main(String[] args) {
		String d = "Java応用だ";

		// 任意の区間の文字列を切り出す
		System.out.println("d.substring(0, 4) → " + d.substring(0, 2));
		System.out.println("d.substring(2, 4) → " + d.substring(2, 4));
		System.out.println("d.substring(1, 7) → " + d.substring(3, 7));
	}
}